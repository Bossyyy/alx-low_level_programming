#include <stdio.h>
#include <stdlib.h>
/**
 * main - This prints the name of the program
 * Then a new line follows
 * @argc: shows the number of command line arguments
 * @argv: a string that shows arguments passed to the main function.
 * Return: 0
 */

int main(int __attribute__((__unused__)) argc, char *argv[])
{
	printf("%s\n", argv[0]);

	return (0);
}
